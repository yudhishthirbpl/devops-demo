package com.example.demo.model;

public class Car {
	
	private String make;
	private String model;
	private String modelYear;
	private String modelRegion;
	private String modelDesc;
	private String co2e;
	/**
	 * @return the co2e
	 */
	public String getCo2e() {
		return co2e;
	}
	/**
	 * @param co2e the co2e to set
	 */
	public void setCo2e(String co2e) {
		this.co2e = co2e;
	}
	/**
	 * @return the make
	 */
	public String getMake() {
		return make;
	}
	/**
	 * @param make the make to set
	 */
	public void setMake(String make) {
		this.make = make;
	}
	/**
	 * @return the model
	 */
	public String getModel() {
		return model;
	}
	/**
	 * @param model the model to set
	 */
	public void setModel(String model) {
		this.model = model;
	}
	/**
	 * @return the modelYear
	 */
	public String getModelYear() {
		return modelYear;
	}
	/**
	 * @param modelYear the modelYear to set
	 */
	public void setModelYear(String modelYear) {
		this.modelYear = modelYear;
	}
	/**
	 * @return the modelRegion
	 */
	public String getModelRegion() {
		return modelRegion;
	}
	/**
	 * @param modelRegion the modelRegion to set
	 */
	public void setModelRegion(String modelRegion) {
		this.modelRegion = modelRegion;
	}
	/**
	 * @return the modelDesc
	 */
	public String getModelDesc() {
		return modelDesc;
	}
	/**
	 * @param modelDesc the modelDesc to set
	 */
	public void setModelDesc(String modelDesc) {
		this.modelDesc = modelDesc;
	}

	
	
}//end of class
